void PlikZAdresatami::zaktualizujDaneWybranegoAdresata(Adresat adresat)
{
    int idEdytowanegoAdresata = adresat.pobierzId();
    fstream odczytywanyPlikTekstowy, tymczasowyPlikTekstowy;
    string nazwaTymczasowegoPlikuZAdresatami = "Adresaci_tymczasowy.txt";
    int numerWczytanejLinii= 1;
    string wczytanaLinia = "";
    string liniaZDanymiAdresataOddzielonePionowymiKreskami = "";
    odczytywanyPlikTekstowy.open(nazwaPlikuZAdresatami.c_str(), ios::in);
    tymczasowyPlikTekstowy.open(nazwaTymczasowegoPlikuZAdresatami.c_str(), ios::out | ios::app);

    if (odczytywanyPlikTekstowy.good() == true && idEdytowanegoAdresata != 0)
    {
        while(getline(odczytywanyPlikTekstowy,wczytanaLinia ))
        {
            if(idEdytowanegoAdresata == pobierzIdAdresataZDanychOddzielonychPionowymiKreskami(wczytanaLinia))
            {
                liniaZDanymiAdresataOddzielonePionowymiKreskami = zamienDaneAdresataNaLinieZDanymiOddzielonymiPionowymiKreskami(adresat);
                if (numerWczytanejLinii == 1)
                    tymczasowyPlikTekstowy << liniaZDanymiAdresataOddzielonePionowymiKreskami;
                else if (numerWczytanejLinii > 1)
                    tymczasowyPlikTekstowy << endl << liniaZDanymiAdresataOddzielonePionowymiKreskami;
            }
            else
            {
                if (numerWczytanejLinii == 1)
                    tymczasowyPlikTekstowy << wczytanaLinia;
                else if (numerWczytanejLinii > 1)
                    tymczasowyPlikTekstowy << endl << wczytanaLinia;

            }
            numerWczytanejLinii++;
        }
        cout << endl << "Dane zostaly zaktualizowane." << endl << endl;
    }
    odczytywanyPlikTekstowy.close();
    tymczasowyPlikTekstowy.close();

    usunPlik(nazwaPlikuZAdresatami);
    zmienNazwePliku(nazwaTymczasowegoPlikuZAdresatami, nazwaPlikuZAdresatami);
}
